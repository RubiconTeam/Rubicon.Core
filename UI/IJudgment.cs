using Rubicon.Core.Data;

namespace Rubicon.Core.UI;

/// <summary>
/// An interface for a class that displays the type of <see cref="HitType"/> the player got.
/// </summary>
public interface IJudgment
{
    /// <summary>
    /// Plays an animation depending on the hit type provided.
    /// </summary>
    /// <param name="type">The <see cref="HitType"/> provided</param>
    /// <param name="offset">A <see cref="Vector2"/> that offsets the position from its default anchors</param>
    public void Play(HitType type, Vector2? offset);
    
    /// <summary>
    /// Plays an animation depending on the hit type provided, giving access to the anchors and offset too.
    /// </summary>
    /// <param name="type">The hit type provided</param>
    /// <param name="anchorLeft">The left anchor (usually from 0 to 1)</param>
    /// <param name="anchorTop">The top anchor (usually from 0 to 1)</param>
    /// <param name="anchorRight">The right anchor (usually from 0 to 1)</param>
    /// <param name="anchorBottom">The bottom anchor (usually from 0 to 1)</param>
    /// <param name="offset">Where to offset the judgment from the anchors, in pixels.</param>
    public void Play(HitType type, float anchorLeft, float anchorTop, float anchorRight, float anchorBottom, Vector2? offset);
}

