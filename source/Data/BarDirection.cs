namespace Rubicon.Core.Data;

/// <summary>
/// Defines which direction a bar would go.
/// </summary>
public enum BarDirection
{
    /// <summary>
    /// Makes the left point "0" and the right point "1".
    /// </summary>
    LeftToRight,
    
    /// <summary>
    /// Makes the left point "1" and the right point "0".
    /// </summary>
    RightToLeft
}